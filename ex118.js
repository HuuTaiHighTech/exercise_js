const givenNumber = 15;
const lowerBound = 10;
const upperBound = 20;

if (givenNumber >= lowerBound && givenNumber <= upperBound) {
    console.log(`${givenNumber} is in the range [${lowerBound}, ${upperBound}]`);
} else {
    console.log(`${givenNumber} is not in the range [${lowerBound}, ${upperBound}]`);
}
